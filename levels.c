int find_level_from_code(char *code)
	{
	int level = 0, check, matched;
	while(level < MAX_LEVELS)
		{
		matched = 0;
		check = 0;
		while(check < LEVEL_CODE_SIZE)
			{
			if(code[check]==levels[level].code[check])
				matched++;
			else
				break;
			
			check++;
			}
			
		if(matched==LEVEL_CODE_SIZE)
			return level + 1;
			
		level++;
		}
	
	level = 1;
	return level;
	}
